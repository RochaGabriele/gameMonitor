package modelo;

public class Senha {
	private int id;
	private String senhaAtual;
	private String senhaPassada;
	private String userSenha;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSenhaAtual() {
		return senhaAtual;
	}
	public void setSenhaAtual(String senhaAtual) {
		this.senhaAtual = senhaAtual;
	}
	public String getSenhaPassada() {
		return senhaPassada;
	}
	public void setSenhaPassada(String senhaPassada) {
		this.senhaPassada = senhaPassada;
	}
	public String getUserSenha() {
		return userSenha;
	}
	public void setUserSenha(String userSenha) {
		this.userSenha = userSenha;
	}
	
		
	
	
	
}
