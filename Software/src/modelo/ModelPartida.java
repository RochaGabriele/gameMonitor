package modelo;

public class ModelPartida {
	private int id;
	private String data;
	private String hora;
	private String local;
	private String nomeTime;
	private String timeAdversario;
	private String nomeArbitro;
	private String nomeApoio;
	private String nomeAuxiliar;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getHora() {
		return hora;
	}
	public void setHora(String hora) {
		this.hora = hora;
	}
	public String getLocal() {
		return local;
	}
	public void setLocal(String local) {
		this.local = local;
	}
	public String getNomeTime() {
		return nomeTime;
	}
	public void setNomeTime(String nomeTime) {
		this.nomeTime = nomeTime;
	}
	public String getTimeAdversario() {
		return timeAdversario;
	}
	public void setTimeAdversario(String timeAdversario) {
		this.timeAdversario = timeAdversario;
	}
	public String getNomeArbitro() {
		return nomeArbitro;
	}
	public void setNomeArbitro(String nomeArbitro) {
		this.nomeArbitro = nomeArbitro;
	}
	public String getNomeApoio() {
		return nomeApoio;
	}
	public void setNomeApoio(String nomeApoio) {
		this.nomeApoio = nomeApoio;
	}
	public String getNomeAuxiliar() {
		return nomeAuxiliar;
	}
	public void setNomeAuxiliar(String nomeAuxiliar) {
		this.nomeAuxiliar = nomeAuxiliar;
	}
	
	
}
