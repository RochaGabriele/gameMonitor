package modelo;

public class ModelPartida {
	private String data;
	private String horario;
	private String localidade;
	private String nomeTime;
	private String nomeAdversario;
	private String nomeArbitro;
	private String nomeApoio;
	private String nomeAuxiliar;
	
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getHorario() {
		return horario;
	}
	public void setHorario(String horario) {
		this.horario = horario;
	}
	public String getLocalidade() {
		return localidade;
	}
	public void setLocalidade(String localidade) {
		this.localidade = localidade;
	}
	public String getNomeTime() {
		return nomeTime;
	}
	public void setNomeTime(String nomeTime) {
		this.nomeTime = nomeTime;
	}
	public String getNomeAdversario() {
		return nomeAdversario;
	}
	public void setNomeAdversario(String nomeAdversario) {
		this.nomeAdversario = nomeAdversario;
	}
	public String getNomeArbitro() {
		return nomeArbitro;
	}
	public void setNomeArbitro(String nomeArbitro) {
		this.nomeArbitro = nomeArbitro;
	}
	public String getNomeApoio() {
		return nomeApoio;
	}
	public void setNomeApoio(String nomeApoio) {
		this.nomeApoio = nomeApoio;
	}
	public String getNomeAuxiliar() {
		return nomeAuxiliar;
	}
	public void setNomeAuxiliar(String nomeAuxiliar) {
		this.nomeAuxiliar = nomeAuxiliar;
	}
	
	
}
